# Korrekturfaktor berechnen
def kor(zahl):
    return zahl * MULTIPLIKATOR

# Spielelement zeichnen
def element_zeichnen(spalte, reihe, farbe):
    pygame.draw.rect(fenster, farbe, [kor(spalte) + 1, kor(reihe) + 1, kor(1) - 1, kor(1) - 1])


# Spielerposition und Geschwindigkeit
player_pos = [WIDTH // 2, HEIGHT // 2]
player_speed = 3

# Laden des Spielerbildes
spielerfigur = pygame.image.load("static/images/TopG.png")

# Schleife Hauptprogramm
while spielaktiv:
    # Überprüfen, ob Nutzer eine Aktion durchgeführt hat
    for event in pygame.event.get():
        if event.type == pygame.QUIT or (event.type == pygame.KEYDOWN and event.key == pygame.K_ESCAPE):
            spielaktiv = False
            print("Spieler hat beendet")

    # Spielerbewegung
    keys = pygame.key.get_pressed()
    x = int(player_pos[0]/MULTIPLIKATOR)
    y = int(player_pos[1]/MULTIPLIKATOR)
    if keys[pygame.K_LEFT]:
        if Karte [y+1][x]!=1:
            player_pos[0] -= player_speed 
    if keys[pygame.K_RIGHT]:
        if Karte [y+1][x+2]!=1:
            player_pos[0] += player_speed
    if keys[pygame.K_UP]:
        if Karte [y][x+1]!=1:
            player_pos[1] -= player_speed
    if keys[pygame.K_DOWN]:
        if Karte [y+2][x+1]!=1:
            player_pos[1] += player_speed

    # Spielfeld zurücksetzen
    fenster.fill(SCHWARZ)

    # Mauersteine zeichnen
    for x in range(62):
        for y in range(33):
            if Karte[y][x] == 0:
                element_zeichnen(x, y, GRAU)
            if Karte[y][x] == 1:
                element_zeichnen(x, y, SCHWARZ)
            if Karte[y][x] == 2:
                element_zeichnen(x, y, WEISS)
            if Karte[y][x] == 3:
                element_zeichnen(x, y, DUNKELGRAU)


    def handle_collisions():
        global player_speed
        x = player_pos[0]
        y = player_pos[1]
        if Karte [int(y/MULTIPLIKATOR)][int(x/MULTIPLIKATOR)] == 1:
            player_pos = y-1

    


    # Spieler zeichnen
    fenster.blit(spielerfigur, player_pos)

    # Fenster aktualisieren
    pygame.display.flip()

    # Refresh-Zeiten festlegen
    clock.tick(60)

# Pygame beenden
pygame.quit()
